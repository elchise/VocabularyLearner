    //removes the selected word from the bst
    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        if(!chkDisplayList.isSelected()){
            //error if not in list view
            JOptionPane.showMessageDialog(rootPane, "To remove a word, please select it from the list in list view.");
            return;
        }
        
        int ind = lstDisplay.getSelectedIndex();
        if(ind < 0){
            //error if no word is selected
            JOptionPane.showMessageDialog(rootPane, "Please select a word to remove");
        } else {
            //delete the selected word from the tree and list and update the display accordingly
            currentWord = lst.get(ind);
            vocab.delete(currentWord);
            lst.remove(ind);
            lblTotal.setText(vocab.size() + " words total");
            updateDisplay();

        }
    }//GEN-LAST:event_btnRemoveActionPerformed

