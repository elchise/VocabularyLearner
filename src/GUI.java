    //create new entry based on user input and add to tree
    private void btnConfirmNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmNewActionPerformed
        //retrieve input values for the vocab entry
        String w = txtNewWord.getText().toLowerCase();
        String d = txtNewDef.getText();
        int total = (Integer)spnTotal.getValue();
        int correct = (Integer)spnCorrect.getValue();
        
        //check that string values for word and definition are not blank
        if(w.equals("") || d.equals("")){
            //if at least one is blank,  output error and do not add
            JOptionPane.showMessageDialog(rootPane, "Make sure you input values for both the word and definition");
            return;
        } 
        
        //check that the total times studied is greater than/equal to the amount it was correct
        if(total < correct){
            JOptionPane.showMessageDialog(rootPane, "The number of times correct should be less than or equal to the total times studied.");
            return;
        }
        
        //check if word is already in bank, if it is, don't add the new word
        if (!vocab.contains(w)){
        
            //add to new entry to list
            vocab.addBalanced(new VocabEntry(w, d, correct, total));

            //update the list view
            List<VocabEntry> updated = vocab.toList();
            for(VocabEntry v : updated){
                lst.addElement(v);
            }
            
            lblTotal.setText(vocab.size() + " words total");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Vocabulary word already exists in the tree");
        }
        
//        System.out.println(vocab.toList().toString());
        lstDisplay.setListData(vocab.toList().toArray(new VocabEntry[vocab.size()]));
        
        txtDisplay.setText(vocab.display());
                
        //rexit and eturn to home page
        pnlNewWord.setVisible(false);
        pnlHome.setVisible(true);
        
        //reset input values
        txtNewWord.setText("");
        txtNewDef.setText("");
        spnTotal.setValue(0);
        spnCorrect.setValue(0);

    }//GEN-LAST:event_btnConfirmNewActionPerformed

