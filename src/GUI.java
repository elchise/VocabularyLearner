    //update study display to display current word and definition
    private void updateWord(){
        lblDisplayWord.setText(currentWord.getWord());
        lblDisplayDef.setText(currentWord.getDef());
        lblAvg.setText("Average Score: " + currentWord.percent() + "%");
    }
    
    // moves to next word
    private void nextWord(){
        int curInd = lst.indexOf(currentWord);
        
        //if there is next word
        if(curInd+1 < vocab.size()){
            //set current word to the next word and update study display
            currentWord = lst.getElementAt(curInd + 1);
//            System.out.println(currentWord);
            updateWord();
        } else {
            //if no more words, display error and return home
            JOptionPane.showMessageDialog(rootPane, "You have reached the end of the study deck.");
            currentWord = null;
            pnlStudy.setVisible(false);
            pnlHome.setVisible(true);
        }
    }
    
    //updates the tree and list display to match changes to tree
    private void updateDisplay(){
        txtDisplay.setText(vocab.display());
        
        //add elements to list
        List<VocabEntry> updated = vocab.toList();
        lst.removeAllElements();
            for(VocabEntry v : updated){
                lst.addElement(v);
            }
            
        //update total number of words
        lblTotal.setText(vocab.size() + " words total");

    }
